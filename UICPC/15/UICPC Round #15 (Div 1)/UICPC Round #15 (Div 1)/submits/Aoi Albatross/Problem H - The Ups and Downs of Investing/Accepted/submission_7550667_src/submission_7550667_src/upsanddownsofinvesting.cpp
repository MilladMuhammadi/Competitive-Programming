#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
ll s,n,m;
int main(){
	cin >> s >> n >> m;
	ll na=0,ma=0;
	std::vector<ll> num(s+1);
	for(ll i=0;i<s;i++)cin >> num[i];
	for(ll i=n-1;i<s-n+1;i++){
		ll b=1;
		ll f=1;
		ll ij=i;
		ll ia=i;
		while(b<n&&ia>=1){
			if(num[ia]>num[ia-1]){
				b++;
				ia--;
			}
			else
				break;
		}
		while(f<n&&ij<s-1){
			if(num[ij]>num[ij+1]){
				f++;
				ij++;
			}
			else
				break;
		}
		//cout<<f<<b<<endl;
		if(f>=n&&b>=n)na++;
	}
	for(ll i=m-1;i<s-m+1;i++){
		ll b=1;
		ll f=1;
		ll ij=i;
		ll ia=i;
		while(b<m&&ia>=1){
			if(num[ia]<num[ia-1]){
				b++;
				ia--;
			}
			else
				break;
		}
		while(f<m&&ij<s-1){
			if(num[ij]<num[ij+1]){
				f++;
				ij++;
			}
			else
				break;
		}
		if(f>=m&&b>=m)ma++;
	}
	cout<<na<<' '<<ma<<endl;
	return 0;
}