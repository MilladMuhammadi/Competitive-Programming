#include <bits/stdc++.h>
using namespace std;
typedef long long ll;

int main(){
	ll t;
	cin >> t;
	while(t--){
		ll n;
		cin >> n;
		map<ll,ll> sp;
		map<ll,ll,greater<ll>> bp;
		ll tp=-1;
		for(ll q=0;q<n;q++){
			string cm,g;
			ll x,y;
			cin >> cm >> x >> g >> g >> y;
			cm=="buy" ? bp[y]+=x:sp[y]+=x;
			while(sp.size() && bp.size() && (bp.begin()->first >= sp.begin()->first) ){
				tp = sp.begin()->first;
				//cout<<"tp "<<tp<<endl;
				ll diff = min(bp.begin()->second,sp.begin()->second);
				bp.begin()->second -= diff;
				sp.begin()->second -= diff;
				if(bp.begin()->second==0)bp.erase(bp.begin());
				if(sp.begin()->second==0)sp.erase(sp.begin());

			}
			string ans="";
			ans = !sp.empty() ? ans+to_string(sp.begin()->first)+' ':ans+"- ";
			ans = !bp.empty() ? ans+to_string(bp.begin()->first)+' ':ans+"- ";
			ans = tp!=-1 ? ans+to_string(tp):ans+"-";
			cout<<ans<<endl;
		}

	}
	return 0;
}